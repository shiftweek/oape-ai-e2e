"""Delta-aware diff engine and scoring for benchmark comparisons.

Compares generated output against ground truth at file-level and
Go AST-level. Detects cases where the tool outperformed the human.
"""

import json
import logging
import re
import statistics
import subprocess
from pathlib import Path

from models import (
    BenchmarkResult,
    FileClassification,
    GenerationResult,
    GroundTruth,
    IterationScore,
    OutperformanceFinding,
)

logger = logging.getLogger(__name__)

MARKER_RE = re.compile(r'//\s*\+kubebuilder:\S+')
STRUCT_RE = re.compile(r'type\s+(\w+)\s+struct\s*\{')
FIELD_RE = re.compile(
    r'\s+(\w+)\s+(\S+.*?)\s+`(.*?)`'
)
FUNC_RE = re.compile(r'func\s+(?:\(\w+\s+\*?\w+\)\s+)?(\w+)\s*\(')
CONST_RE = re.compile(r'\s+(\w+)\s+\w+\s*=\s*"([^"]*)"')

AUTO_GENERATED_PATTERNS = [
    "zz_generated",
    "bundle/manifests/",
    "bundle/metadata/",
    "config/crd/bases/",
    "config/crd/patches/",
    "bundle.Dockerfile",
    "config/samples/kustomization.yaml",
]

FORMATTING_ONLY_EXTENSIONS = [
    ".yaml", ".yml",
]

VALIDATION_MARKERS = [
    "+kubebuilder:validation:Required",
    "+kubebuilder:validation:Optional",
    "+kubebuilder:validation:Enum",
    "+kubebuilder:validation:Pattern",
    "+kubebuilder:validation:Minimum",
    "+kubebuilder:validation:Maximum",
    "+kubebuilder:validation:MinItems",
    "+kubebuilder:validation:MaxItems",
    "+kubebuilder:validation:XValidation",
    "+kubebuilder:default",
    "+optional",
]


def _extract_go_elements(content: str) -> dict:
    """Extract Go code elements from file content using regex."""
    structs: list[dict] = []
    functions: list[str] = []
    markers: list[str] = []
    consts: list[str] = []

    for match in STRUCT_RE.finditer(content):
        structs.append({"name": match.group(1), "fields": []})

    for match in FIELD_RE.finditer(content):
        field_info = {
            "name": match.group(1),
            "type": match.group(2).strip(),
            "tags": match.group(3),
        }
        if structs:
            structs[-1]["fields"].append(field_info)

    for match in FUNC_RE.finditer(content):
        functions.append(match.group(1))

    for match in MARKER_RE.finditer(content):
        markers.append(match.group(0).strip().lstrip("// "))

    for match in CONST_RE.finditer(content):
        consts.append(match.group(1))

    return {
        "structs": structs,
        "functions": functions,
        "markers": markers,
        "consts": consts,
    }


def _extract_added_lines(diff_text: str) -> list[str]:
    """Extract only added lines (starting with +) from a unified diff."""
    added = []
    for line in diff_text.split("\n"):
        if line.startswith("+") and not line.startswith("+++"):
            added.append(line[1:])
    return added


def _try_go_ast_helper(filepath: str) -> dict | None:
    """Try to use the Go AST helper for more accurate extraction."""
    helper = Path(__file__).parent / "go_ast_helper" / "main.go"
    if not helper.exists():
        return None
    try:
        result = subprocess.run(
            ["go", "run", str(helper), "extract", "--file", filepath],
            capture_output=True, text=True, timeout=30,
        )
        if result.returncode == 0:
            return json.loads(result.stdout)
    except (subprocess.TimeoutExpired, json.JSONDecodeError, FileNotFoundError):
        pass
    return None


def _is_auto_generated(filepath: str) -> bool:
    """Check if a file is auto-generated by make/build commands."""
    for pattern in AUTO_GENERATED_PATTERNS:
        if pattern in filepath:
            return True
    return False


def _is_formatting_only_change(gen_path: Path, baseline_path: Path | None) -> bool:
    """Check if changes to a file are only whitespace/formatting."""
    if baseline_path is None or not baseline_path.exists():
        return False
    if not gen_path.exists():
        return False
    try:
        result = subprocess.run(
            ["diff", "--ignore-all-space", "--ignore-blank-lines",
             str(baseline_path), str(gen_path)],
            capture_output=True, text=True,
        )
        return result.returncode == 0
    except FileNotFoundError:
        return False


def _classify_extra_files(
    extra_files: set[str],
    gen_result: "GenerationResult",
    truth: "GroundTruth",
) -> FileClassification:
    """Classify files the tool generated that aren't in the ground truth."""
    classification = FileClassification()

    for fpath in sorted(extra_files):
        if _is_auto_generated(fpath):
            classification.auto_generated.append(fpath)
            continue

        gen_path = gen_result.gen_dir / fpath
        if not gen_path.exists():
            classification.genuinely_wrong.append(fpath)
            continue

        if fpath.endswith(".go"):
            content = gen_path.read_text(errors="replace")
            elements = _extract_go_elements(content)

            has_relevant_code = bool(
                elements.get("structs")
                or elements.get("functions")
                or elements.get("consts")
            )

            if has_relevant_code:
                has_tests = "_test.go" in fpath
                has_validation = any("validation" in m.lower() for m in elements.get("markers", []))
                has_new_types = bool(elements.get("structs"))

                if has_tests or has_validation or has_new_types:
                    classification.valuable_extra.append(fpath)
                else:
                    classification.genuinely_wrong.append(fpath)
            else:
                classification.formatting_only.append(fpath)
        elif any(fpath.endswith(ext) for ext in FORMATTING_ONLY_EXTENSIONS):
            if _is_auto_generated(fpath):
                classification.auto_generated.append(fpath)
            else:
                classification.valuable_extra.append(fpath)
        else:
            classification.genuinely_wrong.append(fpath)

    return classification


def _compute_file_metrics(
    gen_files: set[str],
    truth_files: set[str],
) -> tuple[int, int, int]:
    """Compute file-level true positives, false negatives, false positives."""
    tp = len(gen_files & truth_files)
    fn = len(truth_files - gen_files)
    fp = len(gen_files - truth_files)
    return tp, fn, fp


def _compute_element_overlap(gen_elements: dict, truth_elements: dict) -> dict:
    """Compute overlap between generated and truth Go elements."""
    metrics = {}

    gen_struct_names = {s["name"] for s in gen_elements.get("structs", [])}
    truth_struct_names = {s["name"] for s in truth_elements.get("structs", [])}
    if truth_struct_names:
        matched = gen_struct_names & truth_struct_names
        metrics["struct_match_rate"] = len(matched) / len(truth_struct_names)
        metrics["struct_matched"] = sorted(matched)
        metrics["struct_missed"] = sorted(truth_struct_names - gen_struct_names)
        metrics["struct_extra"] = sorted(gen_struct_names - truth_struct_names)
    else:
        metrics["struct_match_rate"] = 1.0

    gen_fields = set()
    for s in gen_elements.get("structs", []):
        for f in s.get("fields", []):
            gen_fields.add(f"{s['name']}.{f['name']}")
    truth_fields = set()
    for s in truth_elements.get("structs", []):
        for f in s.get("fields", []):
            truth_fields.add(f"{s['name']}.{f['name']}")
    if truth_fields:
        matched_fields = gen_fields & truth_fields
        metrics["field_match_rate"] = len(matched_fields) / len(truth_fields)
        metrics["fields_missed"] = sorted(truth_fields - gen_fields)
        metrics["fields_extra"] = sorted(gen_fields - truth_fields)
    else:
        metrics["field_match_rate"] = 1.0

    gen_funcs = set(gen_elements.get("functions", []))
    truth_funcs = set(truth_elements.get("functions", []))
    if truth_funcs:
        metrics["func_match_rate"] = len(gen_funcs & truth_funcs) / len(truth_funcs)
    else:
        metrics["func_match_rate"] = 1.0

    gen_markers = set(gen_elements.get("markers", []))
    truth_markers = set(truth_elements.get("markers", []))
    if truth_markers:
        metrics["marker_match_rate"] = len(gen_markers & truth_markers) / len(truth_markers)
    else:
        metrics["marker_match_rate"] = 1.0

    return metrics


def _detect_outperformance(
    gen_elements: dict,
    truth_elements: dict,
    filepath: str,
    gen_content: str,
    truth_content: str,
) -> list[OutperformanceFinding]:
    """Detect cases where generated code is better than the truth."""
    findings: list[OutperformanceFinding] = []

    gen_markers = set(gen_elements.get("markers", []))
    truth_markers = set(truth_elements.get("markers", []))
    extra_markers = gen_markers - truth_markers

    for marker in extra_markers:
        for vm in VALIDATION_MARKERS:
            if vm in marker:
                findings.append(OutperformanceFinding(
                    category="markers",
                    severity="actionable",
                    file=filepath,
                    description=f"Tool added validation marker missing from human impl: {marker}",
                    generated_snippet=marker,
                ))
                break

    gen_fields_with_tags: dict[str, str] = {}
    for s in gen_elements.get("structs", []):
        for f in s.get("fields", []):
            key = f"{s['name']}.{f['name']}"
            gen_fields_with_tags[key] = f.get("tags", "")

    truth_fields_with_tags: dict[str, str] = {}
    for s in truth_elements.get("structs", []):
        for f in s.get("fields", []):
            key = f"{s['name']}.{f['name']}"
            truth_fields_with_tags[key] = f.get("tags", "")

    common_fields = set(gen_fields_with_tags) & set(truth_fields_with_tags)
    for field_key in common_fields:
        gen_tag = gen_fields_with_tags[field_key]
        truth_tag = truth_fields_with_tags[field_key]
        if "validate:" in gen_tag and "validate:" not in truth_tag:
            findings.append(OutperformanceFinding(
                category="validation",
                severity="actionable",
                file=filepath,
                description=f"Tool added validation tags to {field_key} that human omitted",
                generated_snippet=gen_tag,
                truth_snippet=truth_tag,
            ))

    gen_comment_lines = len([l for l in gen_content.split("\n") if l.strip().startswith("//")])
    truth_comment_lines = len([l for l in truth_content.split("\n") if l.strip().startswith("//")])
    if gen_comment_lines > truth_comment_lines * 1.5 and gen_comment_lines - truth_comment_lines > 5:
        findings.append(OutperformanceFinding(
            category="docs",
            severity="informational",
            file=filepath,
            description=(
                f"Tool generated significantly more documentation "
                f"({gen_comment_lines} vs {truth_comment_lines} comment lines)"
            ),
        ))

    return findings


def _should_exclude_from_comparison(filepath: str) -> bool:
    """Exclude vendor and e2e test files from comparison."""
    if filepath.startswith("vendor/"):
        return True
    for pattern in ("test/e2e/", "tests/e2e/", "e2e/", "_e2e_test.go", "_e2e_suite_test.go"):
        if pattern in filepath:
            return True
    return False


def compare_iteration(
    gen_result: GenerationResult,
    truth: GroundTruth,
) -> tuple[IterationScore, list[OutperformanceFinding]]:
    """Compare a single iteration's output against ground truth."""
    gen_all_files = {f for f in set(gen_result.files_created + gen_result.files_modified)
                     if not _should_exclude_from_comparison(f)}
    truth_all_files = {f for f in set(truth.files_added + truth.files_modified)
                       if not _should_exclude_from_comparison(f)}

    go_truth_files = {f for f in truth_all_files if f.endswith(".go")}
    go_gen_files = {f for f in gen_all_files if f.endswith(".go")}

    tp, fn, fp = _compute_file_metrics(go_gen_files, go_truth_files)

    completeness_scores: list[float] = []
    convention_scores: list[float] = []
    all_outperformance: list[OutperformanceFinding] = []

    matched_files = go_gen_files & go_truth_files
    for fpath in matched_files:
        gen_path = gen_result.gen_dir / fpath
        truth_path = truth.path / fpath

        if not gen_path.exists() or not truth_path.exists():
            continue

        gen_content = gen_path.read_text(errors="replace")
        truth_content = truth_path.read_text(errors="replace")

        gen_elements = _extract_go_elements(gen_content)
        truth_elements = _extract_go_elements(truth_content)

        overlap = _compute_element_overlap(gen_elements, truth_elements)

        completeness_scores.append(
            (overlap.get("struct_match_rate", 0) * 0.4)
            + (overlap.get("field_match_rate", 0) * 0.4)
            + (overlap.get("func_match_rate", 0) * 0.2)
        )

        convention_scores.append(overlap.get("marker_match_rate", 0))

        outperf = _detect_outperformance(
            gen_elements, truth_elements, fpath, gen_content, truth_content
        )
        all_outperformance.extend(outperf)

    extra_files = gen_all_files - truth_all_files
    classification = _classify_extra_files(extra_files, gen_result, truth)

    for fpath in classification.valuable_extra:
        if fpath.endswith(".go"):
            gen_path = gen_result.gen_dir / fpath
            if gen_path.exists():
                gen_content = gen_path.read_text(errors="replace")
                gen_elements = _extract_go_elements(gen_content)
                if gen_elements.get("structs") or gen_elements.get("functions"):
                    all_outperformance.append(OutperformanceFinding(
                        category="extra_coverage",
                        severity="informational",
                        file=fpath,
                        description=(
                            f"Tool generated additional file with "
                            f"{len(gen_elements.get('structs', []))} structs, "
                            f"{len(gen_elements.get('functions', []))} functions "
                            f"that human did not create"
                        ),
                    ))

    missed_go_files = go_truth_files - go_gen_files
    for _missed in missed_go_files:
        completeness_scores.append(0.0)

    completeness = statistics.mean(completeness_scores) * 100 if completeness_scores else 0.0
    convention = statistics.mean(convention_scores) * 100 if convention_scores else 0.0

    score = IterationScore(
        iteration=gen_result.iteration,
        completeness=round(completeness, 1),
        convention_compliance=round(convention, 1),
        build_success=gen_result.build_success,
        files_matched=tp,
        files_missed=fn,
        genuinely_wrong=len(classification.genuinely_wrong),
        valuable_extras=len(classification.valuable_extra),
        auto_generated=len(classification.auto_generated),
        file_classification=classification,
    )

    return score, all_outperformance


def compare_all_iterations(
    gen_results: list[GenerationResult],
    truth: GroundTruth,
    ep_url: str,
    repo_url: str,
    description: str,
    implementation_prs: list[int],
) -> BenchmarkResult:
    """Compare all iterations and compute aggregate + consistency metrics."""
    all_scores: list[IterationScore] = []
    all_outperformance: list[OutperformanceFinding] = []

    for gen in gen_results:
        score, outperf = compare_iteration(gen, truth)
        all_scores.append(score)
        all_outperformance.extend(outperf)

    dedup_findings: list[OutperformanceFinding] = []
    seen_descriptions: set[str] = set()
    for f in all_outperformance:
        key = f"{f.file}:{f.description}"
        if key not in seen_descriptions:
            seen_descriptions.add(key)
            dedup_findings.append(f)

    completeness_values = [s.completeness for s in all_scores]

    score_variance = {}
    if len(completeness_values) > 1:
        score_variance["completeness"] = round(statistics.stdev(completeness_values), 2)

    best_iter = max(all_scores, key=lambda s: s.completeness - s.genuinely_wrong * 5).iteration

    stable: list[str] = []
    unstable: list[str] = []

    if len(gen_results) > 1:
        per_iter_files = [
            set(g.files_created + g.files_modified)
            for g in gen_results
        ]
        all_files_union = set().union(*per_iter_files) if per_iter_files else set()
        for f in all_files_union:
            present_count = sum(1 for pf in per_iter_files if f in pf)
            if present_count == len(gen_results):
                stable.append(f)
            else:
                unstable.append(f)

    return BenchmarkResult(
        ep_url=ep_url,
        repo_url=repo_url,
        description=description,
        implementation_prs=implementation_prs,
        iteration_scores=all_scores,
        outperformance_findings=dedup_findings,
        stable_elements=sorted(stable),
        unstable_elements=sorted(unstable),
        score_variance=score_variance,
        best_iteration=best_iter,
        median_completeness=round(statistics.median(completeness_values), 1),
    )
